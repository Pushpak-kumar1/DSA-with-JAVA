package YouTube;

import java.util.Scanner;

public class ForLoop1st {
    public static void main (String args[]) {
        Scanner sc = new Scanner(System.in);

        //Counter++ => Counter = Counter + 1
        for (int i = 0; i <= 10; i++) {
            System.out.print(i + " ");
            sc.close();
        }

    }
    
}
