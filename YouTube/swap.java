package YouTube;

public class swap {
        public static void main(String args[]) {
            int a=6;
            int b=5;
    
            a = a + b; // 5+6=11
            b = a - b; // 11-6=5
            a = a - b; // 11-5=6
            
            System.out.println(a);
            System.out.println(b);
        }
    }